name = "zeeekkeee".upper() # upper() method converts the string to uppercase
print(name)

lower_name = "Zeeekkeee".lower() # lower() method converts the string to lower
print(lower_name)

capitalize = "zeeekkeee".capitalize() # capitalize() method converts the string to capitalize format
print(capitalize)

countString = capitalize.lower().count('e') # count the passed substring length of the string
print(countString)
